    public Table readExcelData(File file) throws IOException, InvalidFormatException {
        XSSFWorkbook workbook = new XSSFWorkbook(file);
        Sheet sheet = workbook.getSheetAt(0);
        Table table = Table.create(file.getName());
        for (int i = 0; i < sheet.getLastRowNum(); i++) {
            Row row = sheet.getRow(i);
            for (int j = 0; j < 10; j++) {
                if (i == 0) {
                    table.addColumns(DoubleColumn.create(row.getCell(j).getStringCellValue()));
                } else {
                    DoubleColumn column = (DoubleColumn) table.column(j);
                    column.append(row.getCell(j).getNumericCellValue());
                }
            }
        }
        workbook.close();
        return table;
    }

