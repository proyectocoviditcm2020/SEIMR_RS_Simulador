    private void loadCombo() {
        String[] columnNames = originalData.columnNames().toArray(new String[originalData.columnNames().size()]);
        jComboBoxD.setModel(new DefaultComboBoxModel<>(columnNames));
        jComboBoxR.setModel(new DefaultComboBoxModel<>(columnNames));
        for (int i = 0; i < columnNames.length; i++) {
            String columnName = columnNames[i];
            if (columnName.contains("R")) {
                jComboBoxR.setSelectedIndex(i);
            } else if (columnName.contains("D")) {
                jComboBoxD.setSelectedIndex(i);
            }
        }
        jComboDay.setEnabled(originalData.rowCount() != 365);
        if (originalData.rowCount() == 365) {
            jComboDay.setSelectedIndex(0);
        }
        if (jComboBoxD.getModel().getSize() > 0 && jComboBoxD.getSelectedIndex() == -1) {
            jComboBoxD.setSelectedIndex(0);
            jComboBoxR.setSelectedIndex(0);
        }
    }

