    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (originalData != null) {
            int population = Integer.parseInt(jTextFieldPob.getText());
            int iterations = Integer.parseInt(jTextFieldIter.getText());
            int executions = Integer.parseInt(jTextFieldExecutions.getText());
            population = Math.max(population, 2);
            iterations = Math.max(iterations, 1);
            executions = Math.max(executions, 1);
            Algorithm algorithm = null;
            Problem problem = null;
            _type = null;
            
            parent.modelo = new ModelSEIMRRS2();
            if (jRadioButtonDeath.isSelected()) {
                _type = Covid19.SerieType.DEATHS;
                problem = new Covid19(parent.modelo, parent.region, parent.estrato, parent.getjComboBoxEscenarios().getSelectedIndex(), originalData, jComboDay.getSelectedIndex(), jComboBoxD.getSelectedIndex(), _type);
            } else if (jRadioButtonRecovery.isSelected()) {
                _type = Covid19.SerieType.RECOVERY;
                problem = new Covid19(parent.modelo, parent.region, parent.estrato, parent.getjComboBoxEscenarios().getSelectedIndex(), originalData, jComboDay.getSelectedIndex(), jComboBoxR.getSelectedIndex(), _type);
            } else if (jRadioButtonDR.isSelected()) {
                _type = Covid19.SerieType.DEATHS_RECOVERY;
                problem = new Covid19DR(parent.modelo, parent.region, parent.estrato, parent.getjComboBoxEscenarios().getSelectedIndex(), originalData, jComboDay.getSelectedIndex(), jComboBoxD.getSelectedIndex(), jComboBoxR.getSelectedIndex());
            }
            Crossover crossover = new SBXCrossover(.9, 20);
            Mutation mutation = new PolynomialMutation();

            algorithm = new MoGeA(problem, population, iterations, 0.05, .25, crossover, mutation);

            if (taskWorker == null || taskWorker.isDone()) {
                if (_type == Covid19.SerieType.DEATHS) {
                    taskWorker = new TaskWorker(algorithm, executions, jTextFieldMAE, jProgressBar1, jButton4, gZetaI0, gZetaI1, gZetaI2, gZetaI3, gSigmaI0, gSigmaI1, gSigmaI2, gSigmaI3, MIUUB);
                    jTextFieldPHIX0.setText("");
                    jTextFieldPHIX1.setText("");
                    jTextFieldPHIX2.setText("");
                    jTextFieldPHIX3.setText("");
                    jTextFieldMIUN.setText("");
                } else if (_type == Covid19.SerieType.RECOVERY) {
                    taskWorker = new TaskWorker(algorithm, executions, jTextFieldMAE, jProgressBar1, jButton4, gZetaI0, gZetaI1, gZetaI2, gZetaI3, gSigmaI0, gSigmaI1, gSigmaI2, gSigmaI3, jTextFieldPHIX0, jTextFieldPHIX1, jTextFieldPHIX2, jTextFieldPHIX3, jTextFieldMIUN);
                    MIUUB.setText("");
                } else {
                    taskWorker = new TaskWorker(algorithm, executions, jTextFieldMAE, jProgressBar1, jButton4, gZetaI0, gZetaI1, gZetaI2, gZetaI3, gSigmaI0, gSigmaI1, gSigmaI2, gSigmaI3, jTextFieldPHIX0, jTextFieldPHIX1, jTextFieldPHIX2, jTextFieldPHIX3, MIUUB, jTextFieldMIUN);
                }

                taskWorker.execute();

            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

